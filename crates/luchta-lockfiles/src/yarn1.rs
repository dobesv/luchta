    fn parses_realistic_yarn_v1_fixture() {
        let lockfile = Yarn1Lockfile::parse(FIXTURE).expect("fixture should parse");

        assert!(lockfile.entries_by_key.contains_key("chalk@^4.1.2"));
        assert!(lockfile.entries_by_key.contains_key("left-pad@^1.3.0"));
    }

    #[test]
    fn resolves_known_package() {
        let lockfile = Yarn1Lockfile::parse(FIXTURE).expect("fixture should parse");

        let package = lockfile
            .resolve_package("packages/app", "chalk", "^4.1.2")
            .expect("resolution should succeed")
            .expect("package should exist");

        assert_eq!(package.key, "chalk@^4.1.2");
        assert_eq!(package.version, "4.1.2");
    }

    #[test]
    fn returns_direct_dependencies_for_entry() {
        let lockfile = Yarn1Lockfile::parse(FIXTURE).expect("fixture should parse");

        let dependencies = lockfile
            .all_dependencies("chalk@^4.1.2")
            .expect("dependency lookup should succeed");

        assert_eq!(
            dependencies,
            BTreeMap::from([
                ("ansi-styles".to_string(), "^4.1.0".to_string()),
                ("supports-color".to_string(), "^7.1.0".to_string()),
            ])
        );
    }

    #[test]
    fn missing_package_returns_none() {
        let lockfile = Yarn1Lockfile::parse(FIXTURE).expect("fixture should parse");

        let package = lockfile
            .resolve_package("packages/app", "react", "^18.2.0")
            .expect("resolution should succeed");

        assert!(package.is_none());
    }


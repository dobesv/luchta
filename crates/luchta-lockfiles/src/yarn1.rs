use std::collections::BTreeMap;

use yarn_lock_parser::parse_str;

use crate::{Lockfile, LockfileError, Package};

#[derive(Debug, Clone, PartialEq, Eq)]
struct Yarn1Entry {
    name: String,
    version: String,
    dependencies: BTreeMap<String, String>,
}

/// Yarn v1 lockfile parser.
///
/// `Package.key` uses descriptor form `name@range`, matching one descriptor from
/// parsed `yarn.lock` entry. That key is stable enough to look entry back up for
/// `all_dependencies`.
#[derive(Debug, Clone, Default)]
pub struct Yarn1Lockfile {
    entries_by_key: BTreeMap<String, Yarn1Entry>,
}

impl Yarn1Lockfile {
    /// Creates parsed Yarn v1 lockfile from file contents.
    pub fn parse(content: &str) -> Result<Self, LockfileError> {
        let parsed = parse_str(content).map_err(|error| LockfileError::Parse(error.to_string()))?;
        let mut entries_by_key = BTreeMap::new();

        for entry in parsed.entries {
            let dependencies = entry
                .dependencies
                .into_iter()
                .map(|(name, version)| (name.to_string(), version.to_string()))
                .collect::<BTreeMap<_, _>>();

            for (descriptor_name, descriptor_range) in entry.descriptors {
                let key = format!("{descriptor_name}@{descriptor_range}");
                entries_by_key.insert(
                    key,
                    Yarn1Entry {
                        name: entry.name.to_string(),
                        version: entry.version.to_string(),
                        dependencies: dependencies.clone(),
                    },
                );
            }
        }

        Ok(Self { entries_by_key })
    }
}

impl Lockfile for Yarn1Lockfile {
    fn resolve_package(
        &self,
        _workspace_path: &str,
        name: &str,
        version: &str,
    ) -> Result<Option<Package>, LockfileError> {
        let key = format!("{name}@{version}");
        Ok(self
            .entries_by_key
            .get(&key)
            .map(|entry| Package::new(key, entry.version.clone())))
    }

    fn all_dependencies(&self, key: &str) -> Result<BTreeMap<String, String>, LockfileError> {
        self.entries_by_key
            .get(key)
            .map(|entry| entry.dependencies.clone())
            .ok_or_else(|| LockfileError::InvalidPackageReference(key.to_string()))
    }
}

